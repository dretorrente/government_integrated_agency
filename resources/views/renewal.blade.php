        <h2 class="title">Requirements</h2>
        <p>
        <p>Requirements in driver’s license renewal:
        <p>1.	Current Non-Professional Driver’s License (NPDL), Professional Driver’s License(PDL) or Conductor’s License (CL) may apply for its renewal at least 1 month before its expiration but not more than two (2) years therefrom;
        <h4 class="title">Note: If the expiry date falls on a weekend, holiday or non-working day, the license shall be renewed on or before the last working day prior to the expiry date of such licenses as provided for in the third paragraph of Section 24 of Republic Act No. 4136, otherwise the same shall become delinquent and invalid, except when the license is surrendered to the Director of his deputies on or before the last working day prior to the expiry date of such license as specified, in order to avoid payment of the delinquency fees.</h4>
        <p>2.	Medical Certificates issued by any licensed and practicing physician and valid within fifteen (15) days from the date issued;
        <p>Tips: if you have a poor vision, bring your eyeglasses to avoid hassle
        <p>3.	Tax Identification Number (TIN) (only for professional driver’s license applicants)
        <p>4.	Payment of required fees and charges
        <p>5.	Personal appearance for photo-taking and biometric signature

        <h5 class="title">Venue:</h5>
        <p>Venue for applying a professional driver’s license:
        <p> You can apply at any licensing center/district office with new driver’s license transactions.

        <h2 class="title">procedures</h2>
        <p>Procedures in driver’s license renewal:
        <h6 class="title">Window 1</h6>
        <p>Applicant will submit his/her expired driver’s license together with a Medical Certificate from any Accredited Physician to the LTO Evaluator.
        <p>o	The Evaluator validates if the license is due for renewal and shows a screen shot of the present driver’s license data to the applicant. If the applicant affirms that the information is correct, the evaluator shall return the expired license card to the applicant and shall then transmit the data for picture/signature taking.
        <p>If the applicant wishes to change any information in his/her data and additional restriction, said applicant shall be advised to go to the nearest LTO Licensing Center.
        <p>If the license of the applicant is on alarm, the evaluator will inform the applicant to settle his/her apprehension before his/her license can be renewed.
        <h6 class="title">Window 2</h6>
        <p>Upon receiving the transmitted data from LTO evaluator, the concerned personnel shall take the picture and signature of the applicant. The applicant then proceeds to the last window.
        <h6 class="title">Window 3</h6>
        <p>The Cashier will receive payment and release the new driver’s license card to the applicant. Applicants who submit fake medical certificates shall not be allowed to renew their license, and any license issued on the basis of a fake certificate shall be put on alarm and immediately recalled. The applicant shall also be disqualified from securing a driver’s/conductor’s license in the future in accordance with existing rules.

        <p>Fees and charges in driver’s license renewal:
        <p>License Fee:	P350.00 - P585.00
        <p>Computer Fee:	P67.63
        <p>Total:	P417.63 - P652.63
        <p>Penalties incurred if license expires prior to renewal:
        <p>1 day to 1 year:	P75.00
        <p>1 day and 1 day to 2 years:	P150.00
        <p>More than 2 years:	P225.00

        <h4 class="title">Remember:</h4>
        <p>1.	Never apply with fixers. It’s easy to renew your driver’s license, you can do it on your own. Be punctual, be patient, there are several steps in order for you to obtain a driver’s license.
        <p>2.	Your driver’s license renewal must be on or before the expiry day (your birthday of the expiry year) to avoid penalties and charges.


