        <br/>
        <h2 class="title">Requirements</h2>
        <p>
        <p> I. FOR FIRST TIME PASSPORT APPLICANTS
        <p> GENERAL REQUIREMENTS

        <p> • Personal appearance
        <p> • Confirmed appointment
        <p> • Duly accomplished application form
        <p> • Birth Certificate (BC) in Security Paper (SECPA) issued by the Philippine Statistics Authority (PSA) or Certified True Copy (CTC) of BC issued by the Local Civil Registrar (LCR) and duly authenticated by PSA. Transcribed Birth Certificate from the LCR is required when entries in PSA Birth Certificate are blurred or unreadable. (REPORT OF BIRTH DULY AUTHENTICATED BY PSA IF BORN ABROAD)
        <p> • Valid picture IDs and supporting documents to prove identity (Please refer to List of Acceptable IDs and List of Supporting Documents)

        <p> In Case of No Birth Record

        <p> If born in or after 1950 (January 1, 1950 or after):

        <p> • Apply for the delayed registration of birth at the local civil registry office located at the place of birth of applicant
        <p> • Submit authenticated Birth Certificate from PSA and supporting public document/s with correct date and place of birth (i.e. Form 137, Voter’s Registration Record, Baptismal Certificate with readable dry seal or National Commission on Muslim Filipinos (NCMF) with photo and readable dry seal for Muslim applicants).

        <p> If born before 1950 and below (December 31, 1949 and below):

        <p> • Certificate of Non-Availability of Record from PSA
        <p> • Notarized Joint Birth Affidavit of Two Disinterested Persons
        <p> • Any public document/s with correct full name, date and place of birth (i.e. Baptismal Certificate with readable dry seal or NCMF Certificate with photo and readable dry seal for Muslim applicants)

        <p> For Married Women:

        <p> • Marriage Contract (MC) in Security Paper (SECPA) issued by the Philippine Statistics Authority (PSA) or original and photocopy of the Certified True Copy (CTC) of MC issued by the Local Civil Registrar (LCR) and duly authenticated by PSA. Transcribed Marriage Contract from the LCR is required when entries in PSA Marriage Contract are blurred or unreadable.
        <p> • Report of Marriage duly authenticated by PSA if married abroad
        <p> • Original and photocopy of Commission of Filipino Overseas (CFO) Guidance and Counseling Certificate of Attendance (required for women with foreign national husband).

        <p> Applicants who availed of Dual Citizenship under RA 9225:

        <p> • Identification Certificate of Retention or Re-acquisition
        <p> • Oath of Allegiance
        <p> • Order of Approval

        <p> Applicants who elected Philippine Citizenship:

        <p> • Identification Certificate of Election
        <p> • Oath of Allegiance
        <p> • Affidavit of Election of Philippine Citizenship

        <p> Applicants who has been Naturalized:

        <p> • Identification Certificate of Naturalization
        <p> • Oath of Allegiance

        <p> For Minor Applicants (below 18 years old):

        <p> General Requirements:

        <p> • Confirmed appointment (except for 1 year old and below)
        <p> • Personal appearance of the minor applicant
        <p> • Personal appearance of either parent and valid passport of parents (if minor is a legitimate child)
        <p> • Personal appearance of mother and proper ID or valid passport of mother (if minor is an illegitimate child)
        <p> • Original Birth Certificate of minor in Security Paper issued by PSA or Certified True Copy of Birth Certificate issued by the Local Civil Registrar and duly authenticated by PSA. Transcribed Birth Certificate from the LCR is required when entries in PSA Birth Certificate are blurred or unreadable. Report of Birth duly authenticated by PSA is required if minor was born abroad.
        <p> • Document of identity with photo, if minor is 8-17 years old (for first time and renewal applicant) such as School ID or Form 137 with readable dry seal
        <p> • For minor applicants who never attended school, a Notarized Affidavit of Explanation executed by  either parent (if minor is a legitimate child) / by mother (if minor is an illegitimate child) detailing the reasons why the child is not in school, is required
        <p> • Marriage Certificate of minor’s parents duly authenticated by PSA (for legitimate child)
        <p> • Original and photocopy of valid passport of the person traveling with the minor

        <p> Additional Requirements:

        <p> If minor is not traveling with either parent or alone

        <p> • Personal appearance of either parent (if minor is a legitimate child) / of mother (if minor is an illegitimate child)
        <p> • Affidavit of Support and Consent (ASC) executed by either parent indicating the name of the traveling companion and relationship to the minor. If minor will be traveling alone, ASC must be executed by either parent, stating that his/her child will be traveling alone. If minor is illegitimate, mother should execute the ASC.
        <p> • Original and photocopy of DSWD Clearance
        <p> • There is no need to secure a DSWD Clearance if the minor traveling abroad has parents who are in the Foreign Service or living abroad or are immigrants, provided he / she is holding a valid pass such as a dependent’s visa / pass / identification card or permanent resident visa / pass / identification card which serves as proof that he / she is living with parents abroad.

        <p> If both parents are abroad:

        <p> • Affidavit of Support and Consent (ASC) executed by either parent indicating the name of the traveling companion (authenticated by the nearest Philippine Embassy or Consulate General). If minor is illegitimate, mother should execute the ASC.
        <p> • Special Power of Attorney (SPA) with an attached photocopy of either parent’s valid passport (authenticated by the nearest Philippine Embassy or Consulate General) authorizing a representative in assisting the child to apply for a passport. If minor is illegitimate, mother should execute the SPA.
        <p> • Original and photocopy of DSWD Clearance
        <p> • There is no need to secure a DSWD Clearance if the minor traveling abroad has parents who are in the Foreign Service or living abroad or are immigrants, provided he / she is holding a valid pass such as a dependent’s visa / pass / identification card or permanent resident visa / pass / identification card which serves as proof that he / she is living with parents abroad.
        <p> • Proper ID of the duly authorized representative (Please refer to List of Acceptable IDs)

        <p> If minor is legitimated by subsequent marriage of parents:

        <p> • Authenticated Birth Certificate from PSA must include annotation regarding new status as legitimated and the full name of the child

        <p> If minor is illegitimate but acknowledged by father:

        <p> • Birth Certificate from PSA reflecting surname of father with Affidavit of Acknowledgement and Consent to use the surname of father

        <p> Foundling:

        <p> • Certificate of foundling authenticated by PSA
        <p> • DSWD Clearance
        <p> • Passport of the person who found the applicant
        <p> • Letter of authority or endorsement from DSWD for the issuance of passport

        <p> Orphaned minor applicant:

        <p> • Authenticated Death Certificates of parents from PSA
        <p> • Court order awarding guardianship of the orphaned minor applicant or substitute parental authority under Article 214 & 216 of the Family Code
        <p> • DSWD Clearance

        <p> Abandoned minor applicant:

        <p> • Court order awarding guardianship of the abandoned minor applicant or substitute parental authority
        <p> • DSWD Travel Clearance

        <p> Legally adopted:

        <p> • Original and Certified True Copy (CTC) of PSA Birth Certificate before adoption
        <p> • Original and Certified True Copy (CTC) of the PSA amended Birth Certificate after adoption
        <p> • Certified True Copy (CTC) of the Court Decision or Order on Adoption and Certificate of Finality
        <p> • DSWD clearance for minor applicant, if traveling with the person other than the adopting parents

        <p> In case the applicant is for adoption by foreign parents:

        <p> • Certified True Copy of the Court Decree of Abandonment of Child
        <p> • PSA Death Certificate of the child’s parents or the Deed of Voluntary Commitment executed after the birth of the child
        <p> • Endorsement of child to the Inter-country Adoption Board by the DSWD
        <p> • Authenticated Birth or Foundling Certificate

        <p> Minor applicant whose parents are annulled / divorced:

        <p> • Court order awarding guardianship of the minor applicant or substitute parental authority
        <p> • DSWD Travel Clearance
        <p> • PSA Marriage Certificate with annotation on nullity or annulment decree

        <p> Minor applicant whose mother is likewise a minor:

        <p> • Personal appearance of mother and maternal grandparent/s
        <p> • PSA Birth Certificate of minor applicant and mother
        <p> • Affidavit of Support and Consent executed by the maternal grandparent/s indicating the name of the traveling companion
        <p> • DSWD Clearance if minor will be traveling with the person other than the maternal grandparent/s
        <p> • Proof of identity of mother and maternal grandparent/s (Please refer to List of Acceptable IDs)

        <p> For Muslim Applicants (same general requirements stated above)

        <p> For late registered Muslim applicants:

        <p> • Certificate of Tribal Affiliation from the National Commission on Muslim Filipinos (NCMF)

        <p> For converts who would like to use their Muslim name:

        <p> • Annotated Birth Certificate (BC) in Security Paper (SECPA) issued by the Philippine Statistics Authority (PSA) bearing the Muslim name
        <p> • National Commission on Muslim Filipinos (NCMF) Certificate of Conversion
        </p>

