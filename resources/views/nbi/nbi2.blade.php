        <h2 class="title">Procedures</h2>
        <h6 class="title"> Procedure on How to get NBI Clearance </h6>

        <p> 1. Personally go to the nearest NBI Office in your area. Typically, processing of NBI certificate starts from 8AM to 5PM. If you don’t want to wait for long queuing line, try to go there after lunch break time. There will be less applicants during that time period.
        <p> TIP: Don’t be shy to ask the guards and other people there.
        <p> 2. Ask for an application form and fill out every information that is needed. Be careful to not make any erasures and keep your handwriting clear and readable. Make sure you to double check everything on your application form so you will not be wasting time.
        <p> 3. You need to pay for the corresponding NBI Clearance fee at the counter. There will be another queuing line for the payment so patience is really a must.
        <p> 4. Next, you need to undergo fingerprint scanning and picture-taking. So prepare yourself to look neat on the camera and let all the features on your face be recognized. If everything went smooth, your NBI Clearances will be forwarded to the printing counter and will be released on the same date of application.
        <p> 5. If you have issues along the process of applying for NBI Clearance certificate, the staffs there will forward you to the proper person to deal with. There will be delays but as long as you know to yourself that you’re not involved in any criminal case, you will get your NBI clearance. You just need to be patient.

